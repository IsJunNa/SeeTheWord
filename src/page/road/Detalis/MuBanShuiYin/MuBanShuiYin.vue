        </p>
        <p class="title">主要工艺</p>
        <p class="content">
            木版水印这种纯手工印刷工艺有勾（分版）、刻（制版）、印（印刷）等基本工艺程序和刻、剔、掸、描等特殊技巧，它以笔、刀、刷子、耙子、国画颜料、水等材料为基础工具，以追求复原传统书画的艺术形态、笔墨、神采为目的。</p>
        <p class="title">勾描</p>
        <p class="content">
            由画师担任此项工作。第一步先分色，即把画稿上所有同一色调的笔迹划归于一套版内，画面上有几种色调，即分成几套版。色彩简单的画面有二、三套至八、九套版不等，而工细复杂的要分到几十套，大幅的甚至分到几百套到一千六百于套（如《簪花仕女图卷》）。勾描时先用赛璐璐版（即透明薄胶版）覆盖在原作上，照着勾描；然后用极薄的燕皮纸覆在勾描好的赛璐璐版上再描。将描好以后的画稿，极其精细地反复检查其笔触、神韵，同原作有无出入。之后，再从原作的色调和印刷需要分别勾出几套版备用。
        </p>
        <p class="title">刻板</p>
        <p class="content">这是木版水印的第二道重要工序，即把勾在燕皮纸上的画样粘在木板上再进行雕刻。该者除依据墨线雕刻外，还须参看原作，细心领会，持刀如笔，才能把原作的精神和笔法传达得惟妙惟肖。
        </p>
        <p class="title">印刷</p>
        <p class="content">这是木版水印画的最后一道工序，各分版刻成后，逐版套印成画。印刷使用的纸（或绢）、墨、色等材料和原作材料完全一致。</p>
        <p class="content">
            由于印刷用料及装裱形式与原作相同，因而木版水印的复制品具有酷似原作的特殊效果。人们常誉为"艺术再现"，几可乱真。同时由于木版水印全系手工操作，要求水平高，生产过程时间长，产量少，因此，木版水印画也被作为一种艺术品而广受中外艺术爱好者欢迎。
        </p>
        <p class="title">成长历程</p>
        <p class="content">
            木版水印技艺，是从印制诗笺开始的。所谓诗笺，就是带有暗花和格纹的信纸，是传统文人用的东西。明末十竹斋的诗笺不但代表了木刻水印技艺的最高水平，也体现了这个时期典型的绘画风格。进入20世纪30年代，笺纸的颓败之势日渐显现，旧有的店铺已经大多不再印制，技艺高深的技师又相继故去。一向对版画情有独钟的鲁迅敏锐地观察到了这一点，他意识到如战火再起，这项流传千年的技艺恐怕真的就要泯灭了。
        </p>
        <p class="content">
            1933年，郑振铎受鲁迅之托，在京城寻找能够印制精美诗笺的地方。9月中狂飙怒吼、飞沙漫天的一天，感到希望越来越渺茫的郑振铎寻到了琉璃厂南纸店。在荣宝斋上下的配合下，两位先生的愿望最终实现了，这一民族遗产得以留世，多年后，郑振铎先生对此事仍然念念不忘。通过这次印制，荣宝斋又重新积聚了木版水印的技术力量。
        </p>
        <p class="content">为张大千印制《敦煌供养人》</p>
        <p class="content">
            上个世纪40年代文化界的一桩公案，更让荣宝斋的木版水印成了世人关注的焦点。1941年，张大千携家人和弟子西去敦煌研习敦煌壁画。不久传张大千为了临摹竟大面积破坏敦煌壁画的消息，全国震动。对于张大千打掉敦煌壁画的说法很多，一种较为流行的版本是：张大千率弟子们在第20号窟临摹时，张大千发现早已剥落的小块壁画下面，内层隐隐约约有颜色和线条出现，他认为画的下面一定还藏着某种不为人所知的东西。经过一番协商后，张大千决定打掉外面的一层。剥落之前，他先把上面一层的五代壁画临摹了下来。剥落后，展现他面前的果然是一幅敷彩艳丽、行笔敦厚的盛唐壁画。张大千率领门生在敦煌两年多的时间，据说全部花费达"五百根金条"之巨。他先后临摹了276件敦煌壁画。面对众多求画者，以卖画为生的他却不愿出售其中的任何一幅。
        </p>
        <p class="content">
            此时，与张大千交往密切的荣宝斋经理王仁山，提出以木版水印印制他临摹的敦煌壁画。终于，荣宝斋水印技师田永庆试印的《敦煌供养人》赢得了张大千的赞许和信任，也完成了我国雕版印刷由印制笺纸到国画作品的重大转折，震动了整个文化界。然而对于荣宝斋的木版水印来说，这仅仅是牛刀小试，新中国成立后，才有了荣宝斋施展绝技的广阔舞台。
        </p>
