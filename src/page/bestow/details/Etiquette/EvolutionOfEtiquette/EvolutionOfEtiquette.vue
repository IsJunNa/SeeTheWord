            <h1>礼仪演变</h1>
            <p>中国自古以来有礼义之邦美誉，礼学研究在绝大多数时间内受到重视。礼学在传统学问中属于经学，是一门显学。20世纪前，众多礼学家或其他学者研究礼学，主要是对“三礼”（《周礼》、《仪礼》、《礼记》）的研究，尽管作出过不少成绩，但仍局限于“经学式”字词经义阐释，其主要目的在于服务各王朝的礼制建设与具体的礼仪践履，以巩固当时社会制度。这些学者没有从“制度”层面考虑历代礼制变迁与社会制度以及演化的关系，也很少考虑礼制变迁之深层动因。
