            <p>就方法论而言，我们除采纳一般历史学研究方式外，还根据具体研究对象分别采用不同研究手段，如考古学、政治学、文化人类学、仪式学、宗教学、哲学、文献学等，力求从各个角度进行研究，以便对各时期的礼制变迁作出更为客观、准确、深入的认识。比如在研究先秦礼制变迁时，由于传世文献资料的匮乏，我们比较注意吸收当代丰硕的考古成果，又利用新公布的简帛资料进行研究，利用文字学的研究方式，发掘其中蕴含着的礼制内容，从而力图得出一些新的研究结论。对唐宋礼制与宗教的关系，我们既从政治史角度思考问题，也利用了哲学、宗教学、仪式学和社会学理论来阐述，争取获得一些新意。总之，在方法论上，是根据研究对象灵活使用，争取尽可能发掘研究对象的深层意义。
