            <p>20世纪初，中国社会发生重大变革，随着清王朝的崩溃，封建等级制度遭到质疑，更多学者开始关注中华传统礼制与社会制度的关系。当时一些接受西方思想的学者，在反思封建专制主义政体的危害性时，开始对为专制政体服务的传统礼制进行激烈的批判，甚至持完全否定态度。于是，经学的“显学”地位被打破，礼学研究一度沉寂，乃至被冷落。当然也有一部分思想保守者，仍对中华传统礼制歌功颂德，抱残守缺，激烈反对批判中华传统礼制。显然，这两种思想对中华传统礼制的评判都存在着问题。
