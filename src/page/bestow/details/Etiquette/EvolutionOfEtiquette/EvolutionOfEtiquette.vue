            </p>
            <p>十年动乱结束后，随着改革开放的到来，中国融入世界的速度加快，中国传统文化的研究揭开了新的一页，礼学再次回归学者研究视野之中，并获得了较高的关注度。学者不再教条主义地看待中华传统礼制，而是比较客观、理性地加以研究，礼学研究焕发出青春，出现了崭新的面貌。中华传统礼制的研究确实对拨乱反正，重新发掘中华传统礼制的当代价值，并对重新构建新时期的礼仪制度有着重要的作用。
            </p>
            <p>80年代以来，先后出版了杨志刚、陈戍国和吴丽娱三家中华礼制史著作，他们从各个角度对中华传统礼制进行了研究，取得了相当大的突破。但中华传统礼制内涵极其丰富，历史上行用过的具体礼仪更是多得无法准确统计。这些礼仪随着时代变化而变迁，有的消失在历史长河之中，有的延续至今仍然影响着国人的行为。
            </p>
