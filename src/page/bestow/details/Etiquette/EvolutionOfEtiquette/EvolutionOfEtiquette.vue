            <p>研究中华传统礼制也需要弄清一些基本概念，以便在此基础上作深入、准确与科学的探讨。学界有些相关研究确实没有正确区分礼制与礼俗、礼制与礼仪、礼仪与仪节，往往把这些概念混为一谈，因而得出似是而非的结论。基于此，有必要对以下一些概念进行规范：礼是人际交往之工具，是人与人、人与自然（神）交往行为的规范与准则。礼制是由权力机构颁布的人与人、人与自然（神）、社会集团与社会集团之间交往行为的规范或准则。礼俗是非权力机构颁布的人与人、人与自然（神）、社会集团与社会集团之间交往行为的规范与准则。礼仪是礼制的具体表现形式，礼义是礼制的内在精神实质。仪节则是行用礼仪时的步骤及形体行为。在严格区分礼、礼制、礼俗、礼仪、礼义、仪节的基础上，我们一以贯之，力求使自己的研究结论更为科学。
            </p>
            <p>按照传统说法，周公制礼作乐，创建了中华礼制，这当然不正确，但自孔子开创了儒家一派后，中华传统礼制便受到儒家学说的深刻影响，到秦汉后，儒家思想逐渐成为中华传统礼制的理论基石。西晋武帝创建了以五礼为形式的礼制新体系，这是一种依附于封建专制主义政体、以小农经济为基础、以儒家思想为理论指导、凸显君主集权、强调贵贱等级的一种礼制体系，我们称之为集权礼制。这种礼制体系被后世王朝广泛地采纳，一直延续到近现代，影响极其深远。
            </p>
            <img :src="require('@/assets/bestowImg/4.jpg')">
            <p>这里还有一个很重要的问题需要解决，即中华传统礼制与中华民族的关系问题。众所周知，中华礼制最初在中原地区形成，所行用地区是华夏族先民控制的区域，实际上涵盖面并不广泛，即使与华夏族犬牙交错居住的少数民族并不行用这种礼制，周边其他少数民族自然也不行用这种礼制。华夏族是汉族的前身，因此华夏族（乃至后世的汉族）政权颁布的礼制可称为汉式礼制。
            </p>
            <p>秦统一，建立起大一统的封建政权，理论上说大一统国家都掌握着极为广泛的区域，东南西北疆域较为明确，然而即使在这一统治区域中，许多少数民族也并非都行用汉式礼制。魏晋南北朝时期发生了较大变化，汉族在北方建立政权、实行统治的时间并不长，而周边游牧民族陆续进入中原地区，先后建立众多的少数民族政权，实际控制着宽广的北方区域，这一统治区域中有大量汉族民众。我们可以发现，魏晋南北朝时期少数民族政权进入中原后，也都不约而同地、或多或少地采用了汉式礼制。这一现象与魏晋南北朝时期民族大融合关系极为密切。只要翻阅一下史书就可以发现，许多少数民族政权为了获得统治的合理性与正统性，都宣称自己是炎黄子孙，也就是说出现了民族认同的现象，这是中华民族形成过程中极为重要的一个时期。
            </p>
            <p>隋唐继承了魏晋南北朝时期的这一成果，将民族融合推向高潮，乃至唐太宗被称为“天可汗”。唐政权在吸取少数民族文化（当然包括其礼制文化）时不遗余力，从唐代礼乐中可以清楚地看出融入的少数民族因素。我们认为：自先秦以来逐渐形成的以儒家学说为基础的汉式礼制是中华传统礼制发展的主要线索，而随着少数民族与中原汉民族的交往，尤其是少数民族在构建政权后，所颁布的礼制往往是把少数民族因素的礼仪与汉式礼制结合，从而促进了中华传统礼制的发展。当然，少数民族礼制文化与汉式礼制文化并不是天生就能珠联璧合地结合的，而大多是格格不入，乃至发生激烈的冲突与纠革，然而正是随着这些冲突与纠革，随着民族之间交往的深入，民族融合程度得到加强，一部分少数民族礼仪逐渐融入中华传统礼制之中，从而促进了中华传统礼制的演化与进步。因此可以说，中华传统礼制是中华民族共同创造的礼制。
            </p>
            <img :src="require('@/assets/bestowImg/5.jpg')">
            <p>学术界对少数民族政权的具体礼仪有所研究，但总体上说研究颇为薄弱，至今也未见系统的研究成果，这不能不说是个缺憾。对此，我们加强对北朝、辽金元清诸少数民族政权的礼制的研究，分析这些少数民族政权对待汉式礼制的具体态度，研究哪些具体的少数民族礼仪融入中华传统礼制之中，哪些则仍然保持着他们的民族特色，并着重分析某些有代表性的礼仪问题。例如对北朝少数民族政权的礼仪，我们把五郊迎气礼作为重点研究对象，深入地研讨了这些少数民族政权如何看待及吸收自东汉两晋以来的五郊迎气礼，力图揭示游牧民族的礼制向定居农耕民族礼制演化的过程及意义。学界对辽金元礼制的整体研究极为罕见，我们则对辽金元的礼制作了比较系统的研究，力图做一些探索，以供学界参考。总之，少数民族礼制是需要花大力气去研究的，因为中华民族的发展本身便是一部民族融合史。
            </p>
            <p>中华传统礼制是中华先民历数千年而积淀的民族文化之一，蕴含着中华先民的生命经验和生活智慧。当然，其中既包含历久弥新之精华，也必然有随着历史发展而确应抛弃之糟粕。在我们看来，中华传统礼制在历史上具有加强民族认同和国家认同的功效，曾在建设“礼宜乐合”的和谐社会秩序与“万邦协和”的“国际”关系中体现出极强的实用价值，因此，深入研究中华传统礼制，发掘其中精华，不但具有弘扬国魂的历史价值，对中华文化的长久发展具有极其重要的现实意义，也对建立当今和谐社会具有相当的应用价值。
            </p>
            <img :src="require('@/assets/bestowImg/6.jpg')">
            <p>我们认为：中华传统礼制具有历史传承性、民族认同性、地域普适性、体系开放性、异质包容性和道德实践性等特点。历史传承性奠定了中华传统礼制作为民族文化的地位；民族认同性与地域普适性证明了中华传统礼制适应流传于中华大地的根源，是反对民族分裂主义的历史根据和思想武器；体系开放性和异质包容性决定了中华传统礼制能够在当今世界多元文化格局中继续生存并发展；道德实践性则是唤醒国人礼义之心的精神动力，并是它服务于当今社会的实践基础。
            </p>
            <p>中华传统礼制的精华应当能够长久不衰。因为中华传统礼制蕴含中华先民的人生追求，它积蓄着礼义对人心的凝聚，涵摄着“仁以为己任”的担当精神、追求社会和谐和长治久安的信念，因此中华传统礼制的精华完全可以作为构建现代礼仪体系的学理基石，使其为弘扬民族精神、弘扬社会主义核心价值观服务，使之为中华民族的伟大复兴发挥重要作用。
            </p>


